//----------------------------------------------------------------------------//
// Variables                                                                  //
//----------------------------------------------------------------------------//
let _pw_Random_Generator = null;


//----------------------------------------------------------------------------//
// Public Functions                                                           //
//----------------------------------------------------------------------------//
//------------------------------------------------------------------------------
function
pw_Random_Seed(v)
{
    _pw_Random_Generator = (v != undefined && v != null)
        ? _pw_mulberry32(v)
        : _pw_mulberry32(Date.now());
}

//------------------------------------------------------------------------------
function
pw_Random_Number(m, M)
{
    if(m == undefined) {
        m = 0; M = 1;
    } else if(M == undefined) {
        M = m; m = 0;
    }

    let v = _pw_Random_Generator();
    return m + (v * (M - m));
}

//------------------------------------------------------------------------------
function
pw_Random_Int(m, M)
{
    return Math.floor(pw_Random_Number(m, M));
}

//------------------------------------------------------------------------------
function
pw_Random_Element(collection)
{
    const i = pw_Random_Int(0, collection.length);
    return collection[i];
}

//----------------------------------------------------------------------------//
// Private Functions                                                          //
//----------------------------------------------------------------------------//
//------------------------------------------------------------------------------
function
_pw_mulberry32(a)
{
    // Reference:
    //   https://stackoverflow.com/questions/521295/seeding-the-random-number-generator-in-javascript
    return function() {
      var t = a += 0x6D2B79F5;
      t = Math.imul(t ^ t >>> 15, t | 1);
      t ^= t + Math.imul(t ^ t >>> 7, t | 61);
      return ((t ^ t >>> 14) >>> 0) / 4294967296;
    }
}
