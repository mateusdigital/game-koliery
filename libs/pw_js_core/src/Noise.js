function
pw_Noise_Seed(s)
{
    this.noise.seed(s);
}

//------------------------------------------------------------------------------
function
pw_Noise_Perlin2(x, y)
{
    return this.noise.simplex2(x, y);
}

function
pw_Noise_Perlin3(x, y, z)
{
    return this.noise.simplex3(x, y, z);
}
